    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
            String lessonDate = null;

            if (date.getDate() != null) {
                lessonDate = sdf.format(date.getDate());
            } else {
                JOptionPane.showMessageDialog(this, "Please select a valid date.", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String selectedGrade = gradeCMB.getSelectedItem().toString();
            String selectedSubject = subjectCMB.getSelectedItem().toString();

            // Check if any required fields are empty
            if (lesson.getText().isEmpty() || topic.getText().isEmpty() 
                    || learningTA.getText().isEmpty() || selectedGrade.equals("None") 
                    || selectedSubject.equals("None")) {

                JOptionPane.showMessageDialog(this, "Please fill in all fields.", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            LessonController.submitForm(lesson, topic, lessonDate, selectedGrade, selectedSubject, learningTA);

            clearField();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage(), 
                    "Error", JOptionPane.ERROR_MESSAGE);
        }     
    }//GEN-LAST:event_submitBtnActionPerformed

