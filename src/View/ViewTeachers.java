    private void ShowBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowBtnActionPerformed
        try {
            ResultSet r = new DBSearch().searchTeacher();

            if (r != null) {
                DefaultTableModel dtm = (DefaultTableModel) TeacherTable.getModel();
                dtm.setRowCount(0); // Clear existing rows from the table
                
                while (r.next()) {
                    Vector v = new Vector();
                    v.add(r.getString("tName"));
                    v.add(r.getString("nic"));
                    v.add(r.getString("gender"));
                    v.add(r.getString("eduQ"));
                    v.add(r.getString("qName"));
                    v.add(r.getString("dob"));
                    v.add(r.getString("telephone"));
                    v.add(r.getString("email"));
                    dtm.addRow(v); 
                }
                DBConnection.closeCon();
            } else {
                System.out.println("ResultSet is null. Check the searchTeacher() method.");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_ShowBtnActionPerformed

